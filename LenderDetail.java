package hib.dto;


public class LenderDetail {
        private String fullName;
        private Boolean selfStart;
        private int contactNo;
        private String address;
        private int pincode;
        private String vehicleType;
        private String vehicleModel;
        private String vehicleCondition;
        private String vehicleOil;
         private int  rent;

    /**
     * @return the fullName
     */
    public String getFullName() {
        return fullName;
    }

    /**
     * @param fullName the fullName to set
     */
    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    /**
     * @return the selfStart
     */
    public Boolean getSelfStart() {
        return selfStart;
    }

    /**
     * @param selfStart the selfStart to set
     */
    public void setSelfStart(Boolean selfStart) {
        this.selfStart = selfStart;
    }

    /**
     * @return the contactNo
     */
    public int getContactNo() {
        return contactNo;
    }

    /**
     * @param contactNo the contactNo to set
     */
    public void setContactNo(int contactNo) {
        this.contactNo = contactNo;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the pincode
     */
    public int getPincode() {
        return pincode;
    }

    /**
     * @param pincode the pincode to set
     */
    public void setPincode(int pincode) {
        this.pincode = pincode;
    }

    /**
     * @return the vehicleType
     */
    public String getVehicleType() {
        return vehicleType;
    }

    /**
     * @param vehicleType the vehicleType to set
     */
    public void setVehicleType(String vehicleType) {
        this.vehicleType = vehicleType;
    }

    /**
     * @return the vehicleModel
     */
    public String getVehicleModel() {
        return vehicleModel;
    }

    /**
     * @param vehicleModel the vehicleModel to set
     */
    public void setVehicleModel(String vehicleModel) {
        this.vehicleModel = vehicleModel;
    }

    /**
     * @return the vehicleCondition
     */
    public String getVehicleCondition() {
        return vehicleCondition;
    }

    /**
     * @param vehicleCondition the vehicleCondition to set
     */
    public void setVehicleCondition(String vehicleCondition) {
        this.vehicleCondition = vehicleCondition;
    }

    /**
     * @return the vehicleOil
     */
    public String getVehicleOil() {
        return vehicleOil;
    }

    /**
     * @param vehicleOil the vehicleOil to set
     */
    public void setVehicleOil(String vehicleOil) {
        this.vehicleOil = vehicleOil;
    }

    /**
     * @return the rent
     */
    public int getRent() {
        return rent;
    }

    /**
     * @param rent the rent to set
     */
    public void setRent(int rent) {
        this.rent = rent;
    }
    
}
